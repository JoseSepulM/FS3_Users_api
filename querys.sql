// Creacion de usuario con admin

ALTER SESSION SET "_Oracle_Script"=TRUE;

CREATE USER USERS_API
IDENTIFIED BY "MANG.OSTAS"
DEFAULT TABLESPACE USERS
TEMPORARY TABLESPACE TEMP
QUOTA 100M ON USERS;


 -- PERMISOS PARA EL USUARIO USERS_API
GRANT CREATE SESSION, CREATE TABLE, CREATE USER  TO USERS_API;
GRANT CREATE SEQUENCE TO USERS_API;
GRANT CREATE ROLE TO USERS_API;

GRANT CREATE SESSION TO USERS_API WITH ADMIN OPTION;
GRANT CREATE USER TO USERS_API WITH ADMIN OPTION;
GRANT CREATE VIEW TO USERS_API WITH ADMIN OPTION;
GRANT CREATE ROLE TO USERS_API WITH ADMIN OPTION;


